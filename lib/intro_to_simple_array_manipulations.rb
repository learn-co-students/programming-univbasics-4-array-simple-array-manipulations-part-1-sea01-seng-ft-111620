def using_push(arr, str)
  arr.push(str)
end

def using_unshift(arr, str)
  arr.unshift(str)
end

def using_pop (arr)
  arr.pop
end

def pop_with_args (arr, popCount = 2)
  arr.pop(popCount)
end

def using_shift (arr)
  arr.shift
end

def shift_with_args (arr, shiftCount = 2)
  arr.shift(shiftCount)
end

